package valorAndes.vos;

public class RentabilidadValue {
	//------------------------------------
	//Atributos
	//------------------------------------
	/**
	 * Comportamiento.
	 */
	private String comportamiento;
	
	/**
	 * Descripcion.
	 */
	private String descripcion;
	
	/**
	 * Duracion.
	 */
	private String duracion;
	
	/**
	 * Funcionamiento.
	 */
	private String funcionamiento;
	
	/**
	 * Nombre.
	 */
	private String nombre;
	
	/**
	 * Id unico de la rentabilidad.
	 */
	private int id;

	/**
	 * @return the comportamiento
	 */
	public String getComportamiento() {
		return comportamiento;
	}

	/**
	 * @param comportamiento the comportamiento to set
	 */
	public void setComportamiento(String comportamiento) {
		this.comportamiento = comportamiento;
	}

	/**
	 * @return the descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}

	/**
	 * @param descripcion the descripcion to set
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	/**
	 * @return the duracion
	 */
	public String getDuracion() {
		return duracion;
	}

	/**
	 * @param duracion the duracion to set
	 */
	public void setDuracion(String duracion) {
		this.duracion = duracion;
	}

	/**
	 * @return the funcionamiento
	 */
	public String getFuncionamiento() {
		return funcionamiento;
	}

	/**
	 * @param funcionamiento the funcionamiento to set
	 */
	public void setFuncionamiento(String funcionamiento) {
		this.funcionamiento = funcionamiento;
	}

	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	
	
}
